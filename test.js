/* 1. Function Name: calculateSum */
function calculateSum(a, b) { 
    return a + b;
}
calculateSum(2, 3);
console.log(calculateSum(2, 3));

/* 2. Function Name: isEven */
function isEven(numb) { 
    if (numb % 2 === 0) {
        return true;
    } 
    else {
        return false;
    }
}
isEven(10);
console.log(isEven(10));



